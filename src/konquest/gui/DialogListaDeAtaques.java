    private void btnCancelarAtaqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarAtaqueActionPerformed
        // TODO add your handling code here:
        if (ataqueApuntador != null) {
            ataqueApuntador.cancelarAtaque();
            if (listaAtaquesAgregar.remove(ataqueApuntador)) {
                principal.getManejadorGUI().actualizarToolText(principal, principal.isMapaCiego());
                JOptionPane.showMessageDialog(null, "Ataque cancelado con exito, las naves estan regresando al planeta de origen.", "Ataque", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Lo siento error al cancelar el ataque.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            limpiar();
            updateObservableList(listaAtaquesAgregar);
        } else {
            JOptionPane.showMessageDialog(null, "Lo siento debes de seleccionar un ataque.");
        }
    }//GEN-LAST:event_btnCancelarAtaqueActionPerformed

    private void limpiar(){
        txtNaves.setText("");
        txtPlanetaDetino.setText("");
        txtPlanetaOrigen.setText("");
        txtPorcentajeMuertes.setText("");
        txtTurno.setText("");
       
    }
    
