    public void setAtaqueCancelar(Ataque ataqueCancelar) throws CloneNotSupportedException {
        Ataque previus = this.ataqueCancelar;
        if (ataqueCancelar != null) {
            this.ataqueCancelar = ataqueCancelar.clone();
            txtNaves.setText(Integer.toString(this.ataqueCancelar.getNumeroDeNaves()));
            txtPlanetaDetino.setText(this.ataqueCancelar.getPlanetaDestino().getNombre());
            txtPlanetaOrigen.setText(this.ataqueCancelar.getPlanetaOrigen().getNombre());
            txtPorcentajeMuertes.setText(Double.toString(this.ataqueCancelar.getPorcentajeMuertes()));
            txtTurno.setText(Integer.toString(this.ataqueCancelar.getTurnoLlegada()));
            btnCancelarAtaque.setEnabled(true);
        }
        this.ataqueApuntador = ataqueCancelar;
        firePropertyChange("Ataque Seleccionado", previus, this.ataqueCancelar);
    }
    
    public void updateObservableList(List<Ataque> ataqueListObservable) {
        this.observableListAtaques.clear();
        this.observableListAtaques.addAll(ataqueListObservable);
    }
    
